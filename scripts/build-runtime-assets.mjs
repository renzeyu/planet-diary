import { mkdir, readFile, writeFile } from "node:fs/promises";
import vm from "node:vm";

const projectRoot = new URL("../", import.meta.url);
const archiveRoot = new URL("assets/archive/", projectRoot);
const runtimeRoot = new URL("assets/runtime/", projectRoot);

async function readArchiveGlobal(fileName, globalName) {
  const context = { window: {} };
  const source = await readFile(new URL(fileName, archiveRoot), "utf8");
  vm.runInNewContext(source, context, { filename: fileName });
  return context.window[globalName];
}

function serialize(variableName, value) {
  const json = JSON.stringify(value)
    .replace(/\u2028/g, "\\u2028")
    .replace(/\u2029/g, "\\u2029");
  return `/* Generated by scripts/build-runtime-assets.mjs. Edit the master archive files instead. */\nwindow.${variableName}=${json};\n`;
}

function compactBaseEntry(entry) {
  return {
    id: entry.id,
    title: entry.title,
    date: entry.date,
    year: entry.year,
    story: entry.story,
    tags: entry.tags,
    images: (entry.images || []).map(({ file, width, height }) => ({ file, width, height }))
  };
}

function compactCurationEntry(entry) {
  const compact = { ...entry };
  delete compact.nameSource;
  delete compact.nameConfidence;
  return compact;
}

function compactTranslationEntry(entry) {
  return {
    storyEn: entry.storyEn,
    taglineEn: entry.taglineEn
  };
}

const [archive, curation, translations] = await Promise.all([
  readArchiveGlobal("planet-diary-data.js", "ZEYU_REN_PLANET_DIARY"),
  readArchiveGlobal("planet-diary-curation.js", "ZEYU_REN_PLANET_DIARY_CURATION"),
  readArchiveGlobal("planet-diary-translations.js", "ZEYU_REN_PLANET_DIARY_TRANSLATIONS")
]);

const { entries: archiveEntries, sourceUrl, importedAt, ...archiveMetadata } = archive;
const runtimeArchive = {
  ...archiveMetadata,
  entries: archiveEntries.map(compactBaseEntry)
};
const runtimeCuration = {
  ...curation,
  entries: Object.fromEntries(
    Object.entries(curation.entries).map(([id, entry]) => [id, compactCurationEntry(entry)])
  )
};
const runtimeTranslations = {
  ...translations,
  entries: Object.fromEntries(
    Object.entries(translations.entries).map(([id, entry]) => [id, compactTranslationEntry(entry)])
  )
};

await mkdir(runtimeRoot, { recursive: true });
await Promise.all([
  writeFile(
    new URL("planet-diary-data.js", runtimeRoot),
    serialize("ZEYU_REN_PLANET_DIARY", runtimeArchive)
  ),
  writeFile(
    new URL("planet-diary-curation.js", runtimeRoot),
    serialize("ZEYU_REN_PLANET_DIARY_CURATION", runtimeCuration)
  ),
  writeFile(
    new URL("planet-diary-translations.js", runtimeRoot),
    serialize("ZEYU_REN_PLANET_DIARY_TRANSLATIONS", runtimeTranslations)
  )
]);

console.log(`Built runtime data for ${runtimeArchive.entries.length} planets.`);
